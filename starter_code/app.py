@app.route('/venues')
def venues():
  #num_shows should be aggregated based on number of upcoming shows per venue.
  data = []
  venues = Venue.query.order_by('city', 'state', 'name').all()
  for venue in venues:
    area_item = {}
    pos_area = -1
    if len(data) == 0:
      pos_area = 0
      area_item = {
        "city": venue.city,
        "state": venue.state,
        "venues": []
      }
      data.append(area_item)
    else:
      for i, area in enumerate(data):
        if area['city'] == venue.city and area['state'] == venue.state:
          pos_area = i
          break
      if pos_area < 0:
        area_item = {
          "city": venue.city,
          "state": venue.state,
          "venues": []
        }
        data.append(area_item)
        pos_area = len(data) - 1
      else:
        area_item = data[pos_area]
    v = {
        "id": venue.id,
        "name": venue.name,
        "num_upcoming_shows": 4
      }
    area_item['venues'].append(v)
    data[pos_area] = area_item

  return render_template('pages/venues.html', areas=data)


  
